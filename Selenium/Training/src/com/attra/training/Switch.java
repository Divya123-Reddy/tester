package com.attra.training;

public class Switch {

	public static void main(String[] args) {
		int num = 20;
		switch (num) {
		case 10:
			System.out.println(10);
			break;
		case 11:
			System.out.println(11);
			break;
		case 20:
			System.out.println("I am");
			break;
		default:
			System.out.println("not");
		}

	}

}
